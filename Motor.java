
public class Motor {
	public int numCilindro;
	public int potenci;
	
	Motor (){
		numCilindro = 0;
		potenci = 0;
	}

	public int getNumCilindro() {
		return numCilindro;
	}

	public void setNumCilindro(int numCilindro) {
		this.numCilindro = numCilindro;
	}

	public int getPotenci() {
		return potenci;
	}

	public void setPotenci(int potenci) {
		this.potenci = potenci;
	}
	
}
